add(data.includes("APP_VERSION = '1.0.9'"), 'APP_VERSION is 1.0.9');
add(data.includes('aqua-fantasia-v1.0.9-clean-ui-detail-polish'), 'cache name updated in data');
add(sw.includes('aqua-fantasia-v1.0.9-clean-ui-detail-polish'), 'service worker cache updated');
add(main.includes("v109-clean-ui-detail-polish"), 'visual polish marker is v109');
add(main.includes('v109-clean-detail-screen'), 'runtime menu v109 class exists');
add(main.includes('v109-clean-detail-fishing'), 'fishing v109 class exists');
add(main.includes('swipe-route-out'), 'swipe route feedback exists');
add(main.includes("if (active !== 'fishing'"), 'fishing remains excluded from swipe navigation');
