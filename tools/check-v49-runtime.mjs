import { readFileSync, statSync } from 'node:fs';
import { join } from 'node:path';
const root = process.cwd();
const read = (file) => readFileSync(join(root, file), 'utf8');
const index = read('index.html');
const sw = read('sw.js');
const runtime = read('src/runtime/v49-pixi-runtime.js');
const atlas = JSON.parse(read('assets/atlas/aqua_fishing_v49.atlas.json'));
const requiredFrames = ['bobber','line','ripple','fish','target','spark','meter','button'];
const missingFrames = requiredFrames.filter((key) => !atlas.frames?.[key]);
if (missingFrames.length) throw new Error(`Missing v4.9 atlas frames: ${missingFrames.join(', ')}`);
const requiredIndex = ['initV49Runtime','renderV49RuntimePanel','v49-runtime-panel','v49-pixi-runtime-canvas','aqua_v4.9'];
const missingIndex = requiredIndex.filter((key) => !index.includes(key));
if (missingIndex.length) throw new Error(`Missing v4.9 runtime markers: ${missingIndex.join(', ')}`);
if (!runtime.includes('window.AquaV49Runtime') || !runtime.includes('requestAnimationFrame(draw)')) throw new Error('v4.9 runtime bridge is incomplete');
if (!sw.includes('aqua-fantasia-v4.9.0')) throw new Error('Service worker is not v4.9.0');
if (statSync(join(root, 'assets/atlas/aqua_fishing_v49.webp')).size < 1024) throw new Error('v4.9 WebP atlas is unexpectedly small');
console.log(JSON.stringify({ ok:true, version:'4.9.0', frames:Object.keys(atlas.frames).length, atlasBytes:statSync(join(root, 'assets/atlas/aqua_fishing_v49.webp')).size }, null, 2));
