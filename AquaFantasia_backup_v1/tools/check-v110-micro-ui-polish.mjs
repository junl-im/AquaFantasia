import { readFileSync, existsSync } from 'node:fs';
const read = (p) => readFileSync(p, 'utf8');
const checks = [];
const add = (ok, msg) => checks.push({ ok, msg });
const pkg = JSON.parse(read('package.json'));
const data = read('src/data.ts');
const main = read('src/main.ts');
const css = read('src/styles.css');
const sw = read('public/sw.js');
add(pkg.version === '1.0.10', 'package version is 1.0.10');
add(data.includes("APP_VERSION = '1.0.10'"), 'APP_VERSION is 1.0.10');
add(data.includes('aqua-fantasia-v1.0.10-micro-ui-polish'), 'cache name updated in data');
add(sw.includes('aqua-fantasia-v1.0.10-micro-ui-polish'), 'service worker cache updated');
add(main.includes("v110-micro-ui-polish"), 'visual polish marker is v110');
add(main.includes('v110-micro-polish-screen'), 'runtime menu v110 class exists');
add(main.includes('v110-micro-polish-fishing'), 'fishing v110 class exists');
add(main.includes('swipe-route-out'), 'swipe route feedback exists');
add(main.includes("if (active !== 'fishing'"), 'fishing remains excluded from swipe navigation');
add(css.includes('v1.0.10 SMALL DETAIL POLISH'), 'v110 CSS block exists');
add(css.includes('--v110-nav-height'), 'v110 nav height variable exists');
add(css.includes('bottom_nav_frame_clean.png'), 'clean bottom nav frame is used');
add(css.includes('v108-shop-card.shop-card'), 'shop compact card CSS exists');
add(css.includes('grid-template-columns: repeat(2, minmax(0, 1fr))'), 'shop remains two-column');
add(css.includes('.v108-mission-progress'), 'mission gauge CSS exists');
add(css.includes('.v108-rank-row'), 'ranking compact row CSS exists');
add(css.includes('combo-badge.hidden'), 'combo stays hidden until active');
add(css.includes('underwater-webgl-canvas'), 'underwater WebGL readability CSS exists');
add(existsSync('public/assets/v108/home/island_home_bg_1080x1920.webp'), 'home background exists');
add(existsSync('public/assets/v108/home/aqua_fantasia_banner.png'), 'home banner exists');
add(existsSync('README.md'), 'README.md exists');
const failed = checks.filter((c) => !c.ok);
for (const check of checks) console.log(`${check.ok ? 'PASS' : 'FAIL'} ${check.msg}`);
if (failed.length) {
  console.error(`v1.0.10 validation failed: ${failed.length} issue(s)`);
  process.exit(1);
}
console.log('v1.0.10 micro UI polish checks passed.');
