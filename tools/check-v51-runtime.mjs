import { readFileSync, statSync } from 'node:fs';
import { join } from 'node:path';
const root = process.cwd();
const read = (file) => readFileSync(join(root, file), 'utf8');
const index = read('index.html');
const sw = read('sw.js');
const runtime = read('src/runtime/v51-stability-runtime.js');
if (!index.includes('initV51Runtime') || !index.includes('renderV51StabilityPanel') || !index.includes('v51-stability-panel')) throw new Error('Missing v5.1 stability markers in index.html');
if (!index.includes('src/runtime/v51-stability-runtime.js')) throw new Error('v5.1 runtime script tag missing');
if (!runtime.includes('window.AquaV51Runtime') || !runtime.includes('requestAnimationFrame(sample)')) throw new Error('v5.1 runtime bridge incomplete');
if (!sw.includes('aqua-fantasia-v5.1.0')) throw new Error('Service worker is not v5.1.0');
