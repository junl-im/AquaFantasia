async function precacheSafely() {
  const cache = await caches.open(CACHE_NAME);
  const results = await Promise.allSettled(PRECACHE.map((url) => cache.add(url)));
  const failed = results.filter((result) => result.status === 'rejected').length;
  if (failed) console.warn(`[AquaFantasia SW] precache skipped ${failed} missing or blocked asset(s)`);
}

self.addEventListener('install', (event) => {
  event.waitUntil((async () => {
    await precacheSafely();
    await self.skipWaiting();
  })());
});

self.addEventListener('activate', (event) => {
  event.waitUntil((async () => {
    const keys = await caches.keys();
    await Promise.all(keys.filter((key) => key.startsWith('aqua-fantasia-') && key !== CACHE_NAME).map((key) => caches.delete(key)));
    if ('navigationPreload' in self.registration) {
      try { await self.registration.navigationPreload.enable(); } catch { /* optional browser optimization */ }
    }
    await self.clients.claim();
  })());
});

self.addEventListener('message', (event) => {
  if (event.data?.type === 'AQUA_FORCE_CACHE_SWEEP') {
    event.waitUntil((async () => {
      const keys = await caches.keys();
      await Promise.all(keys.filter((key) => key.startsWith('aqua-fantasia-') && key !== CACHE_NAME).map((key) => caches.delete(key)));
    })());
  }
});

self.addEventListener('fetch', (event) => {
  if (event.request.method !== 'GET') return;
  const req = event.request;
  if (req.headers.has('range')) return;
  const isHtml = req.mode === 'navigate' || req.headers.get('accept')?.includes('text/html');
  event.respondWith((async () => {
    try {
      if (isHtml) {
        const preload = await event.preloadResponse;
        const res = preload || await fetch(req, { cache: 'no-store' });
        if (res && res.ok && req.url.startsWith(self.location.origin)) {
          const copy = res.clone();
          caches.open(CACHE_NAME).then((cache) => cache.put('./index.html', copy));
        }
        return res;
      }
      const cached = await caches.match(req);
      if (cached) return cached;
      const fresh = await fetch(req);
      if (fresh && fresh.ok && req.url.startsWith(self.location.origin) && fresh.type !== 'opaque') {
        const copy = fresh.clone();
        caches.open(CACHE_NAME).then((cache) => cache.put(req, copy));
      }
      return fresh;
    } catch {
      if (isHtml) return await caches.match('./offline.html') || Response.error();
      return await caches.match(req) || Response.error();
    }
  })());
});
