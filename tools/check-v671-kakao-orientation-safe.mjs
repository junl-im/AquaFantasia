import fs from 'node:fs';
const fail = (msg) => { console.error(`[check-v671] ${msg}`); process.exit(1); };
const must = (path) => { if (!fs.existsSync(path)) fail(`missing ${path}`); return fs.readFileSync(path, 'utf8'); };
const data = must('src/data.ts');
const main = must('src/main.ts');
const css = must('src/styles.css');
const guard = must('src/core/PortraitGuard.ts');
const sw = must('public/sw.js');
const manifest = JSON.parse(must('public/manifest.webmanifest'));
const pkg = JSON.parse(must('package.json'));
for (const token of ['6.7.1', 'aqua-fantasia-v6.7.1-kakao-orientation-safe-mode']) if (!data.includes(token)) fail(`data token missing ${token}`);
for (const token of ['installPortraitCssGuards', 'requestHardPortraitLock', 'applyPortraitViewportMetrics']) if (!main.includes(token)) fail(`main token missing ${token}`);
for (const token of ['isKakaoInAppBrowser', 'isHostileInAppBrowser', 'inapp-css-only', 'getPortraitViewportMetrics', 'hard-portrait', 'portrait-primary']) if (!guard.includes(token)) fail(`PortraitGuard token missing ${token}`);
for (const token of ['portrait-only-game', '--portrait-width', 'is-physical-landscape', 'body[data-screen="login"]']) if (!css.includes(token)) fail(`css token missing ${token}`);
if (manifest.orientation !== 'portrait-primary') fail('manifest orientation must be portrait-primary');
if (manifest.display !== 'fullscreen') fail('manifest display must be fullscreen');
if (pkg.version !== '6.7.1') fail('package version must be 6.7.1');
if (!sw.includes('aqua-fantasia-v6.7.1-kakao-orientation-safe-mode')) fail('service worker cache mismatch');
console.log('[check-v671] Kakao safe orientation + portrait cage OK');
